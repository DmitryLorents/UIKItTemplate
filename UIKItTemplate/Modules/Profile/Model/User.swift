/// Container for user profile information
struct User {
    /// Name of user
    let name: String
    /// Name of user's image
    let image: String
    ///  Amount of user's post
    let postAmount: Int
    /// Amount of user's subscribers
    let subscribersAmount: Int
    /// Amount of user's subscriptions
    let subsriptionsAmount: Int
    ///  User's job title
    let position: String
    /// User's site url
    let url: String
}
