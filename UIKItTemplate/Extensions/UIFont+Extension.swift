import UIKit

/// Add fonts, corresponding to app
public extension UIFont {
    static func makeVerdanaBold14() -> UIFont? {
        UIFont(name: "Verdana-bold", size: 14)
    }

    static func makeVerdanaBold10() -> UIFont? {
        UIFont(name: "Verdana-bold", size: 10)
    }
}
